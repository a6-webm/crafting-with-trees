package main;

public class Craft
{
    public int numOfCrafts;
    public int numRequired;

    public Craft()
    {
        this.numOfCrafts = 0;
        this.numRequired = 0;
    }

    public Craft(int numOfCrafts, int numRequired)
    {
        this.numOfCrafts = numOfCrafts;
        this.numRequired = numRequired;
    }
}
